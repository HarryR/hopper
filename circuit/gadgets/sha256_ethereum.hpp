template <typename FieldT>
pb_variable_array<FieldT> from_bits(std::vector<bool> bits, pb_variable<FieldT> &ZERO)
{
    pb_variable_array<FieldT> acc;

    for (size_t i = 0; i < bits.size(); i++)
    {
        bool bit = bits[i];
        acc.emplace_back(bit ? ONE : ZERO);
    }

    return acc;
}

